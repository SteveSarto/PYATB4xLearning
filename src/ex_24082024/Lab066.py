# no return type no argument
def geet():
    print("Hello steve")
geet()

#No return typ with argument
def greet(name):
    print ("hello", name)
greet("Steve")

#no return type with default argument
def hi(name= "Steve"):
    print("hello", name)
hi()
hi("Sarto")

